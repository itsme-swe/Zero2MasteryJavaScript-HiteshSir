const myArray = [2, 4, 6, 8, 10]            // Declaring array in traditional way

console.log(typeof(myArray));   // Object


const newArray = new Array('Superman', 'Spiderman', 'Ironman', 'Hulk')      // Declaring array using new keyword
console.log(newArray);
// Output: [ 'Superman', 'Spiderman', 'Ironman', 'Hulk' ]


console.log(myArray[2]);        // Accessing values in array 
// Output: 6

console.log(newArray[3]);
// Output: Hulk


